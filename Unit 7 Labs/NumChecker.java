public class NumChecker
{
	public static int check( int x )
	{
   if(x%5 == 0)
   {
   return 1;
   }
   if(x%4==0)
   {
   return 2;
   }
   if(x%3==0)
   {
   return 3;
   }
   else
   {
   return 4;
   }

	}
}